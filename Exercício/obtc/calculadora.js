const primeironumero = prompt('Digite o número:')
const segundonumero = prompt('Digite o segundo número:')
const soma = Number(primeironumero) + Number(segundonumero)
const subtração = Number(primeironumero) - Number(segundonumero)
const multiplicação = Number(primeironumero) * Number(segundonumero)
const divisão = Number(primeironumero) / Number(segundonumero)
alert(

    `\n A soma deu ${soma}
     \n A subtração deu ${subtração}
     \n A multiplicação deu ${multiplicação}
     \n A divisão deu ${divisão}`
)
